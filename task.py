# === –û—á–∏—Å—Ç–∫–∞ summary –æ—Ç —Ç–µ–≥–æ–≤ ===
def clean_summary(text: str, tags: list[str]) -> str:
    for tag in tags:
        text = re.sub(re.escape(tag), '', text, flags=re.IGNORECASE)
    return ' '.join(text.split()).strip()

